    private boolean checaPulsos(int pulsos){
        if (pulsos % 2 ==0){
            return true;
        }else{
            return false;
        }
    }
    
    private String colocarEnCasilla(boolean valor){
        if (valor == true){
            return "O";
        }else{
            return "X";
        }
    }
    
