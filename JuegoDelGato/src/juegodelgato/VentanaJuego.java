    private void verificar(){
        
        //diagonal para jugador X
        
        if(btn1.getText().equals("X") & btn5.getText().equals("X") & btn9.getText().equals("X")){
            this.ganador = 1;            
        }
        
        if(btn3.getText().equals("X") & btn5.getText().equals("X") & btn7.getText().equals("X")){
            this.ganador = 1;            
        }
        
        //diagonal para jugador O
        
        if(btn1.getText().equals("O") & btn5.getText().equals("O") & btn9.getText().equals("O")){
            this.ganador = 2;            
        }
        
        if(btn3.getText().equals("O") & btn5.getText().equals("O") & btn7.getText().equals("O")){
            this.ganador = 2;            
        }
        
        
    }
    
    private boolean ganador(int jugador){
        if(jugador ==1){
            return true;
        }else if(jugador==2){
            return true;
        }else if(jugador==3){
            return true;            
        }else{
            return false;
        }                        
    }
    
    private void mensaje(String mensaje){
        JOptionPane.showMessageDialog(null,mensaje,"Juego del Gato",JOptionPane.INFORMATION_MESSAGE);
    }
    
    
    
    private void terminoJuego(boolean termino){
        String mensaje="";
        
        if(termino){
            
            if(this.ganador ==1){
                mensaje ="El ganador es el jugador X";
                txtJugadorX.setText(String.valueOf(Integer.parseInt(this.txtJugadorX.getText())+1));
            }
            
            if(this.ganador == 2){
                mensaje = "El ganador es el jugador O";
                txtJugadorO.setText(String.valueOf(Integer.parseInt(this.txtJugadorO.getText())+1));
            }
            
            if(this.ganador == 3){
                mensaje = "El ganador es el GATO";
                txtGato.setText(String.valueOf(Integer.parseInt(this.txtGato.getText())+1));
            }
            
            mensaje(mensaje);
            
            reiniciar();
        }
    }
    
    private void verificarGato(){
        if((this.ganador !=2) & (this.ganador != 1) & (!btn1.getText().equals("")) & (!btn2.getText().equals("")) & (!btn3.getText().equals("")) & (!btn4.getText().equals("")) & (!btn5.getText().equals("")) & (!btn6.getText().equals("")) & (!btn7.getText().equals("")) & (!btn8.getText().equals("")) & (!btn9.getText().equals(""))){
            this.ganador = 3;
        }
    }
    
    @Override
    public void run() {
        while(true){
            try {
                verificar();
                terminoJuego(ganador(this.ganador));
                verificarGato();
                sleep(10);
            } catch (InterruptedException ex) {
                
            }
        }
    }
   
